#Python Program to Find the Sum of Natural Numbers
print("The natural number start from 1,2,3,4,5.............")
n1=float(input("Enter the 1st number:"))
n2=float(input("Enter the 2nd number:"))
if n1 and n2 !=0 and n1 >0 and n2>0:
    print("The sum of the natural no:",n1+n2)
else:
    print("One of the number or both are  not natural number")
